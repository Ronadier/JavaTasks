package Task6;

public class Human {
  public int id;
  public String name;
  public String fname;
  public String county;
  public int age;
  public boolean is_teen;

  public Human(int id, String name, String fname, String county, int age, boolean is_teen) {
    this.id = id;
    this.name = name;
    this.fname = fname;
    this.county = county;
    this.age = age;
    this.is_teen = is_teen;
  }
}
